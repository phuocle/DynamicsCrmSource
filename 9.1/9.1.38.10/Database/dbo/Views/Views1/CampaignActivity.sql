    left join [SLABase] [campaignactivity_sla_slaid] on ([ActivityPointerBase].[SLAId] = [campaignactivity_sla_slaid].[SLAId] and [campaignactivity_sla_slaid].OverwriteTime = 0 and [campaignactivity_sla_slaid].ComponentState = 0)
    left join [SLABase] [campaignactivity_sla_slainvokedid] on ([ActivityPointerBase].[SLAInvokedId] = [campaignactivity_sla_slainvokedid].[SLAId] and [campaignactivity_sla_slainvokedid].OverwriteTime = 0 and [campaignactivity_sla_slainvokedid].ComponentState = 0)
