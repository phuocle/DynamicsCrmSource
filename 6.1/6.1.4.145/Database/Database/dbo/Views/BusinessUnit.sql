    [OrganizationId],
    [UserGroupId],
    [Name],
    [Description],
    [DivisionName],
    [FileAsName],
    [TickerSymbol],
    [StockExchange],
    [UTCOffset],
    [CreatedOn],
    [ModifiedOn],
