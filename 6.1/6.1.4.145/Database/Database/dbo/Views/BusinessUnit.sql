    [lk_businessunitbase_modifiedby].[YomiFullName],
    [lk_businessunitbase_modifiedonbehalfby].[YomiFullName],
    [lk_businessunitbase_createdby].[YomiFullName],
    [lk_businessunitbase_createdonbehalfby].[FullName],
    [lk_businessunitbase_createdonbehalfby].[YomiFullName],
    [lk_businessunitbase_modifiedonbehalfby].[FullName],
    [lk_businessunitbase_modifiedby].[FullName],
    [business_unit_parent_business_unit].[Name],
    [TransactionCurrency_BusinessUnit].[CurrencyName],

